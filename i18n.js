const RTL_LANGS = ['ar'];

function setLanguage(lang) {
    const t = TRANSLATIONS[lang];
    if (!t) return;
    localStorage.setItem('hbc-lang', lang);

    // RTL support
    document.documentElement.dir = RTL_LANGS.includes(lang) ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;

    // Update all translatable elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else {
                el.innerHTML = t[key];
            }
        }
    });

    // Update select options
    document.querySelectorAll('[data-i18n-opt]').forEach(el => {
        const key = el.getAttribute('data-i18n-opt');
        if (t[key] !== undefined) el.textContent = t[key];
    });

    // Highlight active language button and update pill label
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('lang-active', btn.dataset.lang === lang);
    });

    const currentLangEl = document.getElementById('currentLang');
    if (currentLangEl) {
        currentLangEl.textContent = lang.toUpperCase();
    }
}

function getBestLanguage() {
    const saved = localStorage.getItem('hbc-lang');
    if (saved && TRANSLATIONS[saved]) return saved;

    const browserLang = (navigator.language || navigator.userLanguage || 'es').toLowerCase();

    if (browserLang.startsWith('ar')) return 'ar';
    if (browserLang.startsWith('fr')) return 'fr';
    if (browserLang.startsWith('en')) return 'en';

    return 'es'; // Default
}

document.addEventListener('DOMContentLoaded', () => {
    const startingLang = getBestLanguage();
    setLanguage(startingLang);

    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
    });
});
